    int periodLength(vector <int> &heaps) {
        sort(heaps.begin(), heaps.end());
        vector<vector<int> > s;
        vector<vector<int> >::iterator p;
        while ( (p = find(s.begin(), s.end(), heaps)) == s.end() ) {
            s.push_back(heaps);
            vector<int> tmp;
            for (int v: heaps) {
                if (v > 1) {
                    tmp.push_back(v-1);
                }
            }
            tmp.push_back((int)heaps.size());
            sort(tmp.begin(), tmp.end());
            heaps = tmp;
        }
        return (int)distance(p, s.end());
    }


