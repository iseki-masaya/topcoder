    long long getNumber(vector <int> apple, vector <int> orange) {
        int N = (int)apple.size();
        int M = 2000000;
        for (int i=0; i<N; ++i) {
            M = min(M, apple[i] + orange[i]);
        }
        long long ans = 0;
        for (int X=1; X<=M; ++X) {
            int A = 0, O = 0;
            for (int i=0; i<N; ++i) {
                A += min(apple[i], X);
                O += min(orange[i], X);
            }
            int mn = min(A, O);
            int mx = max(A, O);
            int k = N*X;
            if (mn >= k)
                ans += k+1;
            else
                ans += min(k, mx) - (k - mn) + 1;
        }
        return ans;
    }


